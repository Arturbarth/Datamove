SET SERVEROUTPUT ON
BEGIN
FOR I IN
  (SELECT 'ALTER TRIGGER "' || TRIGGER_NAME || '" DISABLE ' AS vSQL, TRIGGER_NAME
     FROM USER_TRIGGERS
     WHERE STATUS = 'ENABLED')
LOOP  
  BEGIN
  EXECUTE IMMEDIATE I.vSQL;  
  DBMS_OUTPUT.PUT_LINE('TRIGGER DESATIVADA COM SUCESSO => ' || I.TRIGGER_NAME);
  EXCEPTION
  WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE(SQLERRM);
  END;
END LOOP;
COMMIT;
END;

/*INDICES*/
SET SERVEROUTPUT ON
BEGIN
FOR I IN
  (SELECT 'ALTER TABLE "' || TABLE_NAME || '" DISABLE  CONSTRAINT "' || CONSTRAINT_NAME || '"' AS vSQL,
           CONSTRAINT_NAME
     FROM USER_CONSTRAINTS
     WHERE CONSTRAINT_TYPE = 'R' AND STATUS = 'ENABLED')
LOOP  
  BEGIN
  EXECUTE IMMEDIATE I.vSQL;  
  DBMS_OUTPUT.PUT_LINE('CHAVE ALTERADA COM SUCESSO => ' || I.CONSTRAINT_NAME);
  EXCEPTION
  WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE(SQLERRM);
  END;
END LOOP;
COMMIT;
END;